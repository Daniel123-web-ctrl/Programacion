package poo;

public class Conductores {


private String nombre;
private int codigoEmpleado;
private double horasTrabajadasMes;
private double kilometrajeRecorrido;
private double sueldoBase;


public Conductores(){

nombre=nombre;
 codigoEmpleado=codigoEmpleado;
 horasTrabajadasMes=horasTrabajadasMes;
 kilometrajeRecorrido=kilometrajeRecorrido;
sueldoBase=sueldoBase;

}


public String getNombre() {
    return nombre;
}


public void setNombre(String nombre) {
    this.nombre = nombre;
}



public int getCodigoEmpleado() {
    return codigoEmpleado;
}


public void setCodigoEmpleado(int codigoEmpleado) {
    this.codigoEmpleado = codigoEmpleado;
}



public double getHorasTrabajadasMes() {
    return horasTrabajadasMes;
}


public void setHorasTrabajadasMes(double horasTrabajadasMes) {
    this.horasTrabajadasMes = horasTrabajadasMes;
}



public double getKilometrajeRecorrido() {
    return kilometrajeRecorrido;
}


public void setKilometrajeRecorrido(double kilometrajeRecorrido) {
    this.kilometrajeRecorrido = kilometrajeRecorrido;
}





public double getSueldoBase() {
    return sueldoBase;
}


public void setSueldoBase(double sueldoBase) {
    this.sueldoBase = sueldoBase;
}

}



